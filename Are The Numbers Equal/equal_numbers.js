// function to check if two numbers are equal
function isEqual(n1,n2) {
	if (n1 == n2) {
		return true;
	}
}

// test the function

// choose two numbers
n1 = 1;
n2 = "1";

// display result of the function
if (isEqual(n1,n2)) {
	console.log("The numbers are equal.");
}
else {
	console.log("The numbers are not equal.");
}
